// function declaration
function soma(x, y) {
    return x + y
}

// function declaration - Armazena a função em uma variável ou constante.
const sub = function (x, y) {
    return x - y
}

// named function expression - forma pouco usada, vantagem de nomear as funções para averiguação de erros
const mult = function mult(x, y) {
    return x * y
}
